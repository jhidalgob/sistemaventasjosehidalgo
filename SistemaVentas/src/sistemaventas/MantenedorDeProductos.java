    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
      
        
      int  pos=buscarposicion(txtcodigo.getText());
        int resp = JOptionPane.showConfirmDialog(null, "¿Está seguro de eliminar el producto "+txtdescripcion.getText()+"?");
        if (resp==0) {
             productos.remove(pos);
             limpiar();
        } else {
            if(resp==1){
            
            limpiar();
            }
        }
        
       
        try {
            cargarTabla();
        } catch (SQLException ex) {
            Logger.getLogger(MantenedorDeProductos.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }//GEN-LAST:event_btneliminarActionPerformed

