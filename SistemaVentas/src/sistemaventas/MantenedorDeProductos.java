private void limpiar(){

        txtcodigo.setText("");
        txtdescripcion.setText("");
        txtprecio.setText("");
        txtstockactual.setText("");
        txtstockmin.setText("");
    txtcodigo.requestFocus();
}


    
    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
       boolean existe= verificarprod(txtcodigo.getText());
       
        if (existe==false) {
            
        
        try {
            Producto p= new Producto();
            p.setCodigo(txtcodigo.getText());
            p.setDescripcion(txtdescripcion.getText());
            p.setPrecio((parseInt(txtprecio.getText())));
            p.setStockminimo(parseInt(txtstockmin.getText()));
            p.setStockactual(parseInt(txtstockactual.getText()));
           
            productos.add(p);
            
            
            JOptionPane.showMessageDialog(null,"producto fue guardado Existosamente en el array");
         
            limpiar();
            cargarTabla();
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null,"Error al guardar en el array");
            limpiar();
        }
        } else {
            
            
            JOptionPane.showMessageDialog(null,"Producto ya existe en Array");
             limpiar();
        }
        
    }//GEN-LAST:event_btnguardarActionPerformed

  private boolean verificarprod(String codigo){
    boolean a=false; 
     for(int i = 0 ; i <productos.size();i++) {
            if(productos.get(i).getCodigo().equals(codigo) ) {
               a=true;
            }
     }       return a;
};
    
private Producto buscarproducto(String codigo ){
int posicion=0;
     for(int i = 0 ; i <productos.size();i++) {
            if(productos.get(i).getCodigo().equals(codigo) ) {
                posicion=i;
            } 

}
return productos.get(posicion);
};

private int buscarposicion(String codigo){
int posicion=0;
     for(int i = 0 ; i <productos.size();i++) {
            if(productos.get(i).getCodigo().equals(codigo) ) {
                posicion=i;
            } 

}
return posicion;
};



 public void cargarTabla() throws SQLException{
       
        String columnas[]=new String[]{"CODIGO","DESCRIPCION","PRECIO","STOCK MINIMO","STOCK ACTUAL"};
        tablacontenido=new DefaultTableModel(null,columnas);
         
          Object datos[]=new Object[5];
       
       
            for (int i=0;i<productos.size();i++){
                datos[0]=productos.get(i).getCodigo();
                datos[1]=productos.get(i).getDescripcion();
                datos[2]=Integer.toString(productos.get(i).getPrecio());
                datos[3]=Integer.toString(productos.get(i).getStockactual());
                datos[4]=Integer.toString(productos.get(i).getStockminimo());
        
         tablacontenido.addRow(datos);
        }
       
         
    
       this.jTable3.setModel(tablacontenido);
       
         
        
    } 

 
private void buscar(){
    
       boolean existe= verificarprod(txtcodigo.getText());
      // JOptionPane.showMessageDialog(null,String.valueOf(existe));
   
            if (existe==true) {
             
           Producto retorno =  buscarproducto(txtcodigo.getText()); 
        txtdescripcion.setText(retorno.getDescripcion());
        txtprecio.setText(Integer.toString(retorno.getPrecio()));
        txtstockactual.setText( Integer.toString(retorno.getStockactual()));
        txtstockmin.setText( Integer.toString((retorno.getStockminimo())));
          
           } else {
              
              
              int resp= JOptionPane.showConfirmDialog(null, "Producto no Existe...¿Desea agregar nuevo producto?", "Advertencia", JOptionPane.YES_NO_OPTION);
                if (resp==0) {
                    txtdescripcion.requestFocus();
                } else {
                    limpiar();
                }
               
              }
}

